# Example of for loops

people = ['ris','an']

for name in ['chris','susan']:      #Will print the 2 strings
    print(name)

print()

for index in range (0, 2):          #Will print with in the range of 0 to 2 so it will print 0 and 1
    print(index)

print()    
    
for fname in people:                #populating fname array with people array
    print(fname)            