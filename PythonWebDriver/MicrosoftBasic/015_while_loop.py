# Example of while loops



names = ['ris', 'an' ,'risan']
index = 0

while index < len(names):           #Will run a long as index is smaller than the array
    print(names[index])             #will print the content of the array
    index = index +1                #will increase the index
    print(index)                    #will print the index
    
    
index = 0

print()

